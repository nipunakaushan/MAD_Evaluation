package com.example.mad;

public class Nutrition {

    private String height;
    private String weight;
    private String budget;
    private String spinn;
    private String nplan;
    private String ndu;
    private String nwater;
    private String nemail;




    public Nutrition() {
    }

    public String getHeight() {
        return height;
    }

    public void setHeight(String height) {
        this.height = height;
    }

    public String getWeight() {
        return weight;
    }

    public void setWeight(String weight) {
        this.weight = weight;
    }

    public String getBudget() {
        return budget;
    }

    public void setBudget(String budget) {
        this.budget = budget;
    }

    public String getSpinn() {
        return spinn;
    }

    public void setSpinn(String spinn) {
        this.spinn = spinn;
    }



    public String getNplan() {
        return nplan;
    }

    public void setNplan(String nplan) {
        this.nplan = nplan;
    }

    public String getNdu() {
        return ndu;
    }

    public void setNdu(String ndu) {
        this.ndu = ndu;
    }

    public String getNwater() {
        return nwater;
    }

    public void setNwater(String nwater) {
        this.nwater = nwater;
    }

    public String getNemail() {
        return nemail;
    }

    public void setNemail(String nemail) {
        this.nemail = nemail;
    }
}
